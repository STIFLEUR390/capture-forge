// ---------------------------------------------------------------------------
// IntegrityStatus
// ---------------------------------------------------------------------------

/// Integrity state of a recovered session.
///
/// Determined by triple verification:
/// - `Clean`: All three checks pass, full contiguity.
/// - `Partial`: Contiguous prefix recovered, some trailing chunks lost.
/// - `Incomplete`: First chunk missing or no usable prefix.
#[derive(Debug, Clone, PartialEq, Serialize, Deserialize)]
pub(crate) enum IntegrityStatus {
    Clean,
    Partial,
    Incomplete,
}

impl IntegrityStatus {
    /// Return the human-readable label.
    pub(crate) fn as_label(&self) -> &'static str {
        match self {
            IntegrityStatus::Clean => "Clean",
            IntegrityStatus::Partial => "Partial",
            IntegrityStatus::Incomplete => "Incomplete",
        }
    }

    /// Return the CSS class suffix for this state.
    pub(crate) fn css_class(&self) -> &'static str {
        match self {
            IntegrityStatus::Clean => "integrity-clean",
            IntegrityStatus::Partial => "integrity-partial",
            IntegrityStatus::Incomplete => "integrity-incomplete",
        }
    }

    /// Return the recommended action string.
    pub(crate) fn recommended_action(&self) -> &'static str {
        match self {
            IntegrityStatus::Clean => "restore",
            IntegrityStatus::Partial => "partial",
            IntegrityStatus::Incomplete => "abandon",
        }
    }
}

// ---------------------------------------------------------------------------
// IntegrityReport
// ---------------------------------------------------------------------------

/// Comprehensive integrity report generated by triple verification.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub(crate) struct IntegrityReport {
    pub status: IntegrityStatus,
    pub total_chunks: u32,
    pub verified_chunks: u32,
    pub lost_chunks: u32,
    pub contiguous_prefix: u32,
    pub recommended_action: String,
    pub session_id: String,
    pub detail_message: Option<String>,
}

impl IntegrityReport {
    /// Generate a human-readable summary string.
    pub(crate) fn summary(&self) -> String {
        match self.status {
            IntegrityStatus::Clean => format!(
                "All {} chunks verified and contiguous.",
                self.verified_chunks
            ),
            IntegrityStatus::Partial => format!(
                "Clean — up to chunk {} of {}. {} chunks verified, {} lost.",
                self.contiguous_prefix,
                self.total_chunks,
                self.verified_chunks,
                self.lost_chunks,
            ),
            IntegrityStatus::Incomplete => {
                "This recording could not be fully recovered.".into()
            }
        }
    }

    /// Build the detail message for the integrity badge in the preview page.
    fn build_detail_message(status: &IntegrityStatus, contiguous: u32, total: u32) -> Option<String> {
        match status {
            IntegrityStatus::Clean => None,
            IntegrityStatus::Partial => Some(format!(
                "Clean — up to chunk {} of {}",
                contiguous, total,
            )),
            IntegrityStatus::Incomplete => {
                Some("This recording could not be fully recovered.".into())
            }
        }
    }
}

