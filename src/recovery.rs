/// Check the in_flight lock age.
///
/// Returns `true` if the lock is stale (>30s old), meaning the session is
/// considered crashed. Returns `false` if the lock is fresh or absent.
///
/// This is the pure-logic gate — callers provide the lock timestamp (or None).
pub(crate) fn is_lock_stale(lock_timestamp_ms: Option<f64>, now_ms: f64) -> bool {
    match lock_timestamp_ms {
        Some(ts) => {
            if ts.is_finite() && ts > 0.0 {
                if now_ms > ts {
                    (now_ms - ts) > 30_000.0
                } else {
                    // Lock is in the future or equal — not stale.
                    false
                }
            } else {
                // Invalid timestamp (NaN, negative) — treat as stale.
                true
            }
        }
        None => false, // No lock → no crash suspicion from lock.
    }
}

