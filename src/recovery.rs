/// A mock filesystem that simulates OPFS for native testing.
///
/// Stores session directories, chunk files, and manifests so all recovery
/// logic can be tested with `cargo test`.
#[derive(Debug, Clone)]
pub(crate) struct MockFileSystem {
    /// Discovered session directories.
    pub sessions: Vec<MockSessionDir>,
}

/// A single session directory in the mock filesystem.
#[derive(Debug, Clone)]
pub(crate) struct MockSessionDir {
    pub session_id: String,
    pub manifest: Option<ChunkManifest>,
    pub files: Vec<MockChunkFile>,
}

/// A chunk file in the mock filesystem.
#[derive(Debug, Clone)]
pub(crate) struct MockChunkFile {
    pub index: u32,
    pub extension: ChunkFileExt,
    /// Full content: 32-byte header + payload.
    pub data: Vec<u8>,
}

impl MockChunkFile {
    /// Create a new mock file with a valid header and payload.
    pub fn new(index: u32, extension: ChunkFileExt, payload: &[u8]) -> Self {
        let header = ChunkHeader::new(index, 0.0, payload);
        let header_bytes = header.encode();
        let mut data = Vec::with_capacity(32 + payload.len());
        data.extend_from_slice(&header_bytes);
        data.extend_from_slice(payload);
        Self {
            index,
            extension,
            data,
        }
    }

    /// Create a mock file with a corrupted checksum (for testing).
    pub fn new_corrupted(index: u32, extension: ChunkFileExt, payload: &[u8]) -> Self {
        let mut file = Self::new(index, extension, payload);
        // Corrupt one byte in the payload area (offset 32+).
        if file.data.len() > 33 {
            file.data[33] ^= 0xFF;
        }
        file
    }

    /// Return the actual size of this file on "disk".
    fn actual_size(&self) -> u64 {
        self.data.len() as u64
    }
}

impl MockFileSystem {
    /// Create a new empty mock filesystem.
    pub(crate) fn new() -> Self {
        Self {
            sessions: Vec::new(),
        }
    }

    /// Add a session directory.
    pub(crate) fn add_session(&mut self, session: MockSessionDir) {
        self.sessions.push(session);
    }

    /// Convert the mock filesystem to `SessionDir` entries for recovery.
    pub(crate) fn to_session_dirs(&self) -> Vec<SessionDir> {
        self.sessions
            .iter()
            .map(|s| {
                let files: Vec<ChunkFileEntry> = s
                    .files
                    .iter()
                    .map(|f| ChunkFileEntry {
                        index: f.index,
                        extension: f.extension.clone(),
                        actual_size: f.actual_size(),
                        raw_data: f.data.clone(),
                    })
                    .collect();

                SessionDir {
                    session_id: s.session_id.clone(),
                    files,
                    manifest: s.manifest.clone(),
                }
            })
            .collect()
    }
}

impl Default for MockFileSystem {
    fn default() -> Self {
        Self::new()
    }
}

