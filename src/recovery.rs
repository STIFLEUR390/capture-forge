/// Manages crash recovery: OPFS scanning, triple verification, and restore.
pub(crate) struct RecoveryManager {
    /// Discovered orphan session directories.
    orphan_sessions: Vec<SessionDir>,
    /// Generated integrity report (set after restore).
    report: Option<IntegrityReport>,
}

impl RecoveryManager {
    /// Create a new RecoveryManager with no data.
    pub(crate) fn new() -> Self {
        Self {
            orphan_sessions: Vec::new(),
            report: None,
        }
    }

    /// Return the current integrity report, if any.
    pub(crate) fn report(&self) -> Option<&IntegrityReport> {
        self.report.as_ref()
    }

    /// Return the discovered orphan sessions.
    pub(crate) fn orphan_sessions(&self) -> &[SessionDir] {
        &self.orphan_sessions
    }

    /// Register discovered orphan sessions (called by OPFS scan).
    pub(crate) fn set_orphan_sessions(&mut self, sessions: Vec<SessionDir>) {
        self.orphan_sessions = sessions;
    }

    /// Clear all stored state.
    pub(crate) fn clear(&mut self) {
        self.orphan_sessions.clear();
        self.report = None;
    }
}

impl Default for RecoveryManager {
    fn default() -> Self {
        Self::new()
    }
}

