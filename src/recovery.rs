/// Result of a single chunk's manifest-vs-filesystem check.
#[derive(Debug, Clone, PartialEq)]
pub(crate) enum FileCheck {
    /// File exists with primary extension (.bin).
    Verified,
    /// File exists with secondary extension (.written) — lower confidence.
    WrittenOnly,
    /// File does not exist.
    Lost,
}

/// Result of a single chunk's size match check.
#[derive(Debug, Clone, PartialEq)]
pub(crate) enum SizeCheck {
    /// Actual file size matches expected size (header + payload).
    SizeOk,
    /// Size mismatch.
    SizeMismatch,
}

