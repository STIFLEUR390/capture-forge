/// Represents a discovered session directory with its files.
#[derive(Debug, Clone)]
pub(crate) struct SessionDir {
    pub session_id: String,
    pub files: Vec<ChunkFileEntry>,
    pub manifest: Option<ChunkManifest>,
}

/// One chunk file discovered during OPFS scan.
#[derive(Debug, Clone)]
pub(crate) struct ChunkFileEntry {
    pub index: u32,
    pub extension: ChunkFileExt,
    pub actual_size: u64,
    pub raw_data: Vec<u8>,
}

/// Extension classification for chunk files from file naming.
#[derive(Debug, Clone, PartialEq)]
pub(crate) enum ChunkFileExt {
    Bin,
    Written,
    Partial,
}

