// Triple verification
// ---------------------------------------------------------------------------

/// Results of all three verification checks.
#[derive(Debug, Clone)]
pub(crate) struct TripleVerification {
    /// Per-chunk check 1 results (manifest vs filesystem).
    pub file_checks: Vec<(u32, FileCheck)>,
    /// Per-chunk check 2 results (size match), only for Verified entries.
    pub size_checks: Vec<(u32, SizeCheck)>,
    /// Check 3 result (index contiguity).
    pub contiguous_prefix: u32,
    /// Total chunks in the manifest.
    pub total_chunks: u32,
}

/// Run check 1: verify every committed/written manifest entry has a file.
pub(crate) fn check_manifest_vs_filesystem(
    manifest: &ChunkManifest,
    files: &[ChunkFileEntry],
) -> Vec<(u32, FileCheck)> {
    let mut results: Vec<(u32, FileCheck)> = Vec::new();

    for entry in &manifest.entries {
        // Only consider Written and Committed entries.
        if entry.status != ChunkStatus::Written && entry.status != ChunkStatus::Committed {
            continue;
        }

        let index = entry.chunk_index;
        let check = if files.iter().any(|f| f.index == index && f.extension == ChunkFileExt::Bin) {
            FileCheck::Verified
        } else if files.iter().any(|f| f.index == index && f.extension == ChunkFileExt::Written) {
            FileCheck::WrittenOnly
        } else {
            FileCheck::Lost
        };
        results.push((index, check));
    }

    results
}

/// Run check 2: verify file sizes match manifest entries.
///
/// Only checks entries that passed check 1 (Verified or WrittenOnly).
pub(crate) fn check_size_match(
    file_checks: &[(u32, FileCheck)],
    manifest: &ChunkManifest,
    files: &[ChunkFileEntry],
) -> Vec<(u32, SizeCheck)> {
    let mut results: Vec<(u32, SizeCheck)> = Vec::new();

    for (index, file_check) in file_checks {
        if *file_check == FileCheck::Lost {
            continue;
        }

        // Find the manifest entry.
        let manifest_entry = match manifest.entries.iter().find(|e| e.chunk_index == *index) {
            Some(e) => e,
            None => {
                results.push((*index, SizeCheck::SizeMismatch));
                continue;
            }
        };

        // Find the file entry.
        let file_entry = match files.iter().find(|f| f.index == *index) {
            Some(f) => f,
            None => {
                results.push((*index, SizeCheck::SizeMismatch));
                continue;
            }
        };

        // Expected size: 32-byte header + payload_size from manifest.
        let expected_size = 32u64 + manifest_entry.payload_size;
        let actual_size = file_entry.actual_size;

        if actual_size == expected_size {
            results.push((*index, SizeCheck::SizeOk));
        } else {
            results.push((*index, SizeCheck::SizeMismatch));
        }
    }

    results
}

/// Run check 3: identify the longest contiguous prefix from index 0.
pub(crate) fn check_index_contiguity(
    file_checks: &[(u32, FileCheck)],
    size_checks: &[(u32, SizeCheck)],
    total_chunks: u32,
) -> u32 {
    let mut contiguous = 0u32;

    for i in 0..total_chunks {
        // Check file existence (check 1).
        let file_ok = file_checks
            .iter()
            .any(|(idx, check)| *idx == i && *check != FileCheck::Lost);

        if !file_ok {
            break;
        }

        // Check size match (check 2).
        let size_ok = size_checks
            .iter()
            .any(|(idx, check)| *idx == i && *check == SizeCheck::SizeOk);

        if !size_ok {
            break;
        }

        contiguous += 1;
    }

    contiguous
}

/// Run all three verification checks and produce a `TripleVerification`.
pub(crate) fn triple_verify(
    manifest: &ChunkManifest,
    files: &[ChunkFileEntry],
) -> TripleVerification {
    let file_checks = check_manifest_vs_filesystem(manifest, files);
    let size_checks = check_size_match(&file_checks, manifest, files);
    let contiguous_prefix = check_index_contiguity(
        &file_checks,
        &size_checks,
        manifest.entries.len() as u32,
    );

    TripleVerification {
        file_checks,
        size_checks,
        contiguous_prefix,
        total_chunks: manifest.entries.len() as u32,
    }
}

/// Generate an `IntegrityReport` from triple verification results.
pub(crate) fn generate_report(
    session_id: &str,
    verification: &TripleVerification,
) -> IntegrityReport {
    let verified_chunks = verification
        .size_checks
        .iter()
        .filter(|(_, check)| *check == SizeCheck::SizeOk)
        .count() as u32;

    let lost_chunks = verification
        .file_checks
        .iter()
        .filter(|(_, check)| *check == FileCheck::Lost)
        .count() as u32;

    let (status, contiguous_prefix) = if verification.contiguous_prefix == 0 {
        (IntegrityStatus::Incomplete, 0)
    } else if verification.contiguous_prefix >= verification.total_chunks && lost_chunks == 0 {
        (IntegrityStatus::Clean, verification.total_chunks)
    } else {
        (IntegrityStatus::Partial, verification.contiguous_prefix)
    };

    let recommended_action = status.recommended_action().to_string();
    let detail_message = IntegrityReport::build_detail_message(
        &status,
        contiguous_prefix,
        verification.total_chunks,
    );

    // For Clean status, set total_chunks = verified_chunks (they're equal).
    // For Partial, use the actual total.
    // For Incomplete, all are lost.
    let (total, verified, lost) = match status {
        IntegrityStatus::Clean => (verified_chunks, verified_chunks, 0),
        IntegrityStatus::Partial => (verification.total_chunks, verified_chunks, lost_chunks),
        IntegrityStatus::Incomplete => (verification.total_chunks, 0, lost_chunks),
    };

    IntegrityReport {
        status,
        total_chunks: total,
        verified_chunks: verified,
        lost_chunks: lost,
        contiguous_prefix,
        recommended_action,
        session_id: session_id.to_string(),
        detail_message,
    }
}

/// Collect verified chunks for the export pipeline.
///
/// Returns the valid contiguous prefix chunks. If no chunks can be recovered,
/// returns `Err(RecordingError::EmptySession)`.
pub(crate) fn recover_contiguous_prefix(
    manifest: &ChunkManifest,
    files: &[ChunkFileEntry],
    contiguous_prefix: u32,
) -> Result<Vec<ExportChunk>> {
    if contiguous_prefix == 0 {
        return Err(RecordingError::EmptySession {
            details: "No usable chunks to recover from contiguous prefix".into(),
        });
    }

    let mut export_chunks: Vec<ExportChunk> = Vec::with_capacity(contiguous_prefix as usize);

    for i in 0..contiguous_prefix {
        // Check manifest entry exists for this index.
        if !manifest.entries.iter().any(|e| e.chunk_index == i) {
            return Err(RecordingError::ExportError {
                details: format!(
                    "Manifest entry missing for chunk index {} during recovery",
                    i,
                ),
            });
        }

        // Find the file entry — prefer .bin, fall back to .written.
        let file_entry = files
            .iter()
            .find(|f| f.index == i && f.extension == ChunkFileExt::Bin)
            .or_else(|| files.iter().find(|f| f.index == i && f.extension == ChunkFileExt::Written));

        let file_entry = match file_entry {
            Some(f) => f,
            None => {
                return Err(RecordingError::ExportError {
                    details: format!(
                        "File entry missing for chunk index {} during recovery",
                        i,
                    ),
                });
            }
        };

        // Parse the header from the raw data.
        let header_bytes: [u8; 32] = match file_entry.raw_data[..32].try_into() {
            Ok(b) => b,
            Err(_) => {
                return Err(RecordingError::ExportError {
                    details: format!(
                        "Chunk {}: insufficient data for header during recovery",
                        i,
                    ),
                });
            }
        };

        let header = ChunkHeader::decode(&header_bytes)?;

        // Extract payload (bytes after the 32-byte header).
        let payload = file_entry.raw_data[32..].to_vec();

        // Determine status: if .bin → Committed, if .written → Written.
        let status = if file_entry.extension == ChunkFileExt::Bin {
            ChunkStatus::Committed
        } else {
            ChunkStatus::Written
        };

        export_chunks.push(ExportChunk {
            index: i,
            header,
            payload,
            status,
        });
    }

    Ok(export_chunks)
}

